package pojo;

public class Stand {
	private Integer noOfSeats;

	public Stand() {
		this.noOfSeats = 500;
	}
	public Stand(Integer noOfSeats) {
		super();
		this.noOfSeats = noOfSeats;
	}
	//--------getters & setters-------------
	
	
	

	public Integer getNoOfSeats() {
		return noOfSeats;
	}

	public void setNoOfSeats(Integer noOfSeats) {
		this.noOfSeats = noOfSeats;
	}
	
	
	
}
