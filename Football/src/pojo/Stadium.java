package pojo;
import pojo.Ground;
import pojo.Stand;

public class Stadium {
	private Stand stands;
	private Ground ground;
	
	//gettrs & setters
	public Stand getStands() {
		return stands;
	}
	public void setStands(Stand stands) {
		this.stands = stands;
	}
	public Ground getGround() {
		return ground;
	}
	public void setGround(Ground ground) {
		this.ground = ground;
	}
	
	
	
}
